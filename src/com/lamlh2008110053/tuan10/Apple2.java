package com.lamlh2008110053.tuan10;

public class Apple2 
{
    
    int id;
    String color;
    double weight;
    
    Apple2()
    {

    }


    Apple2(int idd, String c, double w)
    {
        id = idd;
        color = c;
        weight = w;
    }


    public void printfApple2()
    {
        System.out.println("ID: " + id + " " + " Color: " + color + " " + " Weight: " + weight);
    }

    
}
